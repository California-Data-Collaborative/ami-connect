CREATE TABLE SENTRYX_METER_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    DEVICE_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    DEVICE_STATUS VARCHAR(16777216),
    SERVICE_STATUS VARCHAR(16777216),
    STREET VARCHAR(16777216),
    CITY VARCHAR(16777216),
    STATE VARCHAR(16777216),
    ZIP VARCHAR(16777216),
    DESCRIPTION VARCHAR(16777216),
    MANUFACTURER VARCHAR(16777216),
    INSTALL_NOTES VARCHAR(16777216),
    INSTALL_DATE VARCHAR(16777216),
    METER_SIZE VARCHAR(16777216),
    UNIQUE (ORG_ID, DEVICE_ID, CREATED_TIME)
);

CREATE TABLE SENTRYX_READ_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    DEVICE_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    TIME_STAMP VARCHAR(16777216),
    READING VARCHAR(16777216),
    UNITS VARCHAR(16777216),
    UNIQUE (ORG_ID, DEVICE_ID, CREATED_TIME)
);
