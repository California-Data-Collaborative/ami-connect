CREATE TABLE IF NOT EXISTS SUBECA_ACCOUNT_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    accountId VARCHAR(16777216),
    accountStatus VARCHAR(16777216),
    meterSerial VARCHAR(16777216),
    billingRoute VARCHAR(16777216),
    registerSerial VARCHAR(16777216),
    meterSize VARCHAR(16777216),
    createdAt VARCHAR(16777216),
    deviceId VARCHAR(16777216),
    activeProtocol VARCHAR(16777216),
    installationDate VARCHAR(16777216),
    latestCommunicationDate VARCHAR(16777216),
    UNIQUE (ORG_ID, deviceId)
);

CREATE TABLE IF NOT EXISTS SUBECA_DEVICE_LATEST_READ_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    deviceId VARCHAR(16777216),
    usageTime VARCHAR(16777216),
    unit VARCHAR(16777216),
    value VARCHAR(16777216),
    UNIQUE (ORG_ID, deviceId, usageTime)
);

CREATE TABLE IF NOT EXISTS SUBECA_USAGE_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    deviceId VARCHAR(16777216),
    usageTime VARCHAR(16777216),
    unit VARCHAR(16777216),
    value VARCHAR(16777216),
    UNIQUE (ORG_ID, deviceId, usageTime)
);
